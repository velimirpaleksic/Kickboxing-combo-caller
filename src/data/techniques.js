export const stanceMap = {
  Ortodox: {
    leadHand: 'lijeva ruka',
    rearHand: 'desna ruka',
    leadLeg: 'lijeva noga',
    rearLeg: 'desna noga',
  },
  Southpaw: {
    leadHand: 'desna ruka',
    rearHand: 'lijeva ruka',
    leadLeg: 'desna noga',
    rearLeg: 'lijeva noga',
  },
};

export const getTechnique = (id) => techniques[id] || null;

const stanceDetailFor = (technique, stance) => {
  const map = stanceMap[stance] || stanceMap.Ortodox;
  if (technique.side === 'lead' && technique.category === 'punch') return map.leadHand;
  if (technique.side === 'rear' && technique.category === 'punch') return map.rearHand;
  if (technique.side === 'lead') return map.leadLeg;
  if (technique.side === 'rear') return map.rearLeg;
  return technique.side;
};

export const resolveTechniqueIds = (ids, stance = 'Ortodox') =>
  ids
    .map((id) => techniques[id])
    .filter(Boolean)
    .map((technique) => ({
      ...technique,
      stance,
      stanceDetail: stanceDetailFor(technique, stance),
    }));
